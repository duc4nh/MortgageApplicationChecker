package com.fdmgroup.fluffycrmsystem.servlets;

import java.io.IOException;
import javax.servlet.RequestDispatcher;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

public class ClientCategoryRegistrationFormServlet extends HttpServlet {

	@Override
	protected void doGet(HttpServletRequest req, HttpServletResponse resp)
			throws ServletException, IOException {
		// Perform a redirect
		RequestDispatcher dispatcher = req.getRequestDispatcher("../WEB-INF/clientCategoryRegistrationForm.jsp");
		dispatcher.forward(req, resp);
	}
}
