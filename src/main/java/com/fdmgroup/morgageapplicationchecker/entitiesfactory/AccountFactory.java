package com.fdmgroup.morgageapplicationchecker.entitiesfactory;

import com.fdmgroup.morgageapplicationchecker.entities.Account;

public class AccountFactory {

	public Account createAccount(int id, double balanceQ1, double balanceQ2,
			double balanceQ3, double balanceQ4, double interestRate,
			double maxDeposit, String type) {
		Account account = new Account();
		account.setId(id);
		account.setBalanceQ1(balanceQ1);
		account.setBalanceQ2(balanceQ2);
		account.setBalanceQ3(balanceQ3);
		account.setBalanceQ4(balanceQ4);
		account.setInterestRate(interestRate);
		account.setMaxDeposit(maxDeposit);
		account.setType(type);

		return account;

	}

}
