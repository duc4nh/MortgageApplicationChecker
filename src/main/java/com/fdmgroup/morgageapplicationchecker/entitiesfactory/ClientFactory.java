package com.fdmgroup.morgageapplicationchecker.entitiesfactory;

import java.util.Calendar;

import com.fdmgroup.morgageapplicationchecker.entities.Client;

public class ClientFactory {

	public Client createClient(String firstName, String lastName,
			String phoneNumber, String address, Calendar dob) {
		Client client = new Client();
		client.setFirstName(firstName);
		client.setLastName(lastName);
		client.setPhoneNumber(phoneNumber);
		client.setAddress(address);
		client.setDob(dob);

		return client;
	}
}
