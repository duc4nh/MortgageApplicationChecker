package com.fdmgroup.morgageapplicationchecker.entitiesfactory;

import com.fdmgroup.morgageapplicationchecker.entities.CreditHistory;

public class CreditHistoryFactory {

	public CreditHistory createCreditHistory(int id, int ficoScore,
			double annualPercentageRate, double monthlyPayment,
			String totalInterestPaid) {
		CreditHistory creditHistory = new CreditHistory();
		creditHistory.setId(id);
		creditHistory.setFicoScore(ficoScore);
		creditHistory.setAnnualPercentageRate(annualPercentageRate);
		creditHistory.setMonthlyPayment(monthlyPayment);
		creditHistory.setTotalInterestPaid(totalInterestPaid);

		return creditHistory;

	}
}
